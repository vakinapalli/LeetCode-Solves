class Solution:
    def reverseBits(self, n: int) -> int:
        a = 0b11111111111111110000000000000000
        b = 0b00000000000000001111111111111111
        c = 0b11111111000000001111111100000000
        d = 0b00000000111111110000000011111111
        e = 0b11110000111100001111000011110000
        f = 0b00001111000011110000111100001111
        g = 0b11001100110011001100110011001100
        h = 0b00110011001100110011001100110011
        i = 0b10101010101010101010101010101010
        j = 0b01010101010101010101010101010101

        ans = ((n & a) >> 16) | ((n & b) << 16)
        ans = ((ans & c) >> 8) | ((ans & d) << 8)
        ans = ((ans & e) >> 4) | ((ans & f) << 4)
        ans = ((ans & g) >> 2) | ((ans & h) << 2)
        ans = ((ans & i) >> 1) | ((ans & j) << 1)
        return ans